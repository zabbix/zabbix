				&& substr(CTestArrayHelper::get($data, 'key'), 0, 8) === 'mqtt.get') {
			$this->assertFalse($form->getField('Update interval')->isDisplayed());
			$this->assertFalse($form->getField('Custom intervals')->isDisplayed());

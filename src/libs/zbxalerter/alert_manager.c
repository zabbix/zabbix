{
	zbx_uint64_t		id;
	zbx_uint64_t		mediatypeid;

	/* alert queue */
	zbx_binary_heap_t	queue;

	int			location;

	/* number of currently processing alerts */
	int			alerts_num;

	/* number of alert objects for this alert pool */
	int			refcount;
}
zbx_am_alertpool_t;

